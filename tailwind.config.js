// 处理负值
function getNegative(obj) {
    let __obj = {};
    // 负值转换
    for (let key in obj) {
        if (key === 0 || key === "0") {
            __obj[`${key}`] = `${obj[key]}`;
        } else {
            __obj[`-${key}`] = `-${obj[key]}`;
        }
    }

    return __obj;
}
